/**
 * Add two numbers and return the result
 * @param {number} a
 * @param {number} b
 * @returns number
 */
const sum = (a, b) => {
  return a + b;
};

/**
 * Subtract two numbers and return the result
 * @param {number} a
 * @param {number} b
 * @returns number
 */
const sustration = (a, b) => {
  return a - b;
};

/**
 * Multiplication of two numbers and returns the result
 * @param {number} a
 * @param {number} b
 * @returns number
 */
const multiplication = (a, b) => {
  return a * b;
};

/**
 * Divide two numbers and return the result
 * @param {number} a
 * @param {number} b
 * @returns number
 */
const division = (a, b) => {
  return a / b;
};

module.exports = { sum, sustration, multiplication, division };
